package academy.maze.dto;

/** Тип ячейки в лабиринте. Каждый тип имеет стоимость прохождения, влияющую на поиск пути. */
public enum CellType {
    /** Стена - непроходимая ячейка */
    WALL(Integer.MAX_VALUE),

    /** Обычный путь - базовая стоимость прохождения */
    PATH(1),

    /** Болото - ухудшающая поверхность, медленное передвижение */
    SWAMP(3),

    /** Песок - ухудшающая поверхность, затрудненное передвижение */
    SAND(2),

    /** Монета - улучшающая поверхность, бонус за прохождение */
    COIN(0);

    private final int cost;

    CellType(int cost) {
        this.cost = cost;
    }

    /**
     * Возвращает стоимость прохождения через ячейку.
     *
     * @return стоимость прохождения
     */
    public int getCost() {
        return cost;
    }

    /**
     * Проверяет, является ли ячейка проходимой.
     *
     * @return true, если ячейка проходима
     */
    public boolean isPassable() {
        return this != WALL;
    }
}
